#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	src_len;

	src_len = (size_t)ft_strlen((char *)src);
	j = 0;
	i = 0;
	while (dest[i] && size > i)
		i++;
	if (size <= i)
		return (src_len + size);
	while (src[j] && i + j < size - 1)
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	return (i + src_len);
}
